// segunda até sexta => 8 horas
// Sabado => 4 horas
// Domingo => 0 horas

const diaDaSemana = "sexta"; //segunda terca, quarta, quinta, sexta, sabado , domingo

 const SEGUNDA = 'segunda';
 const TERCA = 'terca';
 const QUARTA = 'quarta';
 const QUINTA = 'quinta';
 const SEXTA = 'sexta';
 const SABADO = 'sabado';
 const DOMINGO = 'domingo';

let cargaHoraria;

const eDiaUtil =
  diaDaSemana === SEGUNDA
  ||   diaDaSemana === TERCA
  ||   diaDaSemana === QUARTA 
  ||   diaDaSemana === QUINTA
  ||   diaDaSemana === SEXTA

if (eDiaUtil) {
  cargaHoraria = 8;
} else if (diaDaSemana === SABADO) {
  cargaHoraria = 4;
} else if (diaDaSemana === DOMINGO) {
  cargaHoraria = 0;
}
console.log("A carga horária é " + cargaHoraria + " horas");
